package design_pattern.composite.before;

public class Moniter {
	private int price;
	private int power;
	
	public Moniter(int power, int price) {		
		this.power = power;
		this.price = price;
	}
	public int getPrice() {
		return price;
	}
	public int getPower() {
		return power;
	}
	
	
}
